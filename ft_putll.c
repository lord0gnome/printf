void	ft_putll(long n)
{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n >= 10)
		{
			ft_putll(n / 10);
			ft_putll(n % 10);
		}
		else
			ft_putchar(n + 48);
}
